% 衛星の位置（例：衛星のXYZ座標）
X_s = -14165.81086590;  % 単位：キロメートル
Y_s = 22289.57291196;
Z_s = 782.94593302;

% 受信機の位置（例：受信機のXYZ座標）
X_r = 1000;  % 単位：キロメートル
Y_r = 2000;
Z_r = 3000;

% 光速
c = 3e8;  % m/s

% 受信機のクロック誤差（仮定：0.0001秒）
delta_t = 0.0001;  % 秒

% 衛星と受信機の間の疑似距離を計算
pseudo_range = sqrt((X_s - X_r)^2 + (Y_s - Y_r)^2 + (Z_s - Z_r)^2) + c * delta_t;

% 結果を表示
disp(['疑似距離 (km): ', num2str(pseudo_range)]);
